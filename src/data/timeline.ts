// ─── Helper Functions ─────────────────────────────────────────────────────────

/**
 * Returns all events matching the given category.
 */
export function getEventsByCategory(category: EventCategory): TimelineEvent[] {
  return timelineEvents.filter((e) => e.category === category);
}

/**
 * Returns all events matching the given significance level.
 */
export function getEventsBySignificance(sig: Significance): TimelineEvent[] {
  return timelineEvents.filter((e) => e.significance === sig);
}

/**
 * Returns events whose date falls within [from, to] (inclusive).
 * Dates are compared lexicographically; partial dates (e.g. "1947") compare
 * correctly against full ISO dates because string ordering matches chronological
 * ordering for ISO 8601 date prefixes.
 *
 * @param from - Start date string, e.g. "1989-01-01"
 * @param to   - End date string, e.g. "1989-12-31"
 */
export function getEventsByDateRange(from: string, to: string): TimelineEvent[] {
  return timelineEvents.filter((e) => e.date >= from && e.date <= to);
}
